package mathvoyage;

/**
 * The enum Triangle type.
 */
public enum triangleType {
    /**
     * Equilateral triangle type.
     */
    EQUILATERAL,
    /**
     * Isosceles triangle type.
     */
    ISOSCELES,
    /**
     * Scalene triangle type.
     */
    SCALENE,
    /**
     * Rightangle triangle type.
     */
    RIGHTANGLE,
    /**
     * Obtuse triangle type.
     */
    OBTUSE,
    /**
     * Acute triangle type.
     */
    ACUTE
}
