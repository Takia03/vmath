package mathvoyage.geometry;

/**
 * The enum Distance type.
 */
public enum distanceType {
    /**
     * Cartesian distance type.
     */
    CARTESIAN,
    /**
     * Polar distance type.
     */
    POLAR,
    /**
     * Manhattan distance type.
     */
    MANHATTAN,
    /**
     * Chebyshev distance type.
     */
    CHEBYSHEV;
}
